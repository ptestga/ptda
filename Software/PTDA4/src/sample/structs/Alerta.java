package sample.structs;

public class Alerta {

    private String id, tipo, data, hora;

    public Alerta(String id, String tipo, String data, String hora) {
        this.id = id;
        this.tipo = tipo;
        this.data = data;
        this.hora = hora;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }
}
